namespace Calliope.Core.Enums
{
    /// <summary>
    /// Categories for organizing character traits
    /// </summary>
    public enum TraitCategory
    {
        Temperament,    // How they react emotionally (aggressive, diplomatic)
        Social,         // How they interact with others (leader, loyal)
        Cognitive,      // How they think and process (analytical, wise)
        Moral,          // Their value system (kind)
        Courage,        // Risk tolerance (brave, timid)
        Custom          // User-defined categories
    }
}